"""
Цель кода:
1) Определить матрицу A.
2) Нужно привести матрицу к виду Ax 0 , и решить методом RREF and Gaussian method
- по итогу получаем систему из 2х уравнений. и там 
3) Полученный результат  null space.
4) Показать, что Ax=0 для любых линейных комбинаций этих векторов.
"""

import sympy

def find_null_space_of_A():
    # 1) Определяем матрицу A с помощью sympy.Matrix
    A = sympy.Matrix([
        [3, 1, 9, 4],
        [2, 1, 7, 3],
        [5, 2, 16, 7]
    ])
    
    # 2) Вычисляем null space (нулевое пространство)
    #    Функция .nullspace() возвращает список векторов, которые образуют базис этого подпространства.
    null_space = A.nullspace()
    
    # 3) Выводим результат
    #print("Матрица A:")
    #print(A)
    print("\nБазисные векторы нулевого пространства A (Null Space):")
    
    for i, vec in enumerate(null_space, start=1):
        print(f"Вектор {i}:", vec)

# Запускаем функцию
if __name__ == "__main__":
    find_null_space_of_A()
