
import numpy as np

'A2 Matrix and Vector Operations'

# Определяем матрицу A (3x3)
# A = [ a11  a12  a13 ]
#     [ a21  a22  a23 ]
#     [ a31  a32  a33 ]
A = np.array([[a11, a12, a13], 
              [a21, a22, a23], 
              [a31, a32, a33]])

# Определяем вектор-столбец B (3x1)
# B = [ b1 ]
#     [ b2 ]
#     [ b3 ]
B = np.array([[b1], 
              [b2], 
              [b3]])

# Формула для расчета каждого элемента вектора C:
# C_i1 = A_i1 * B1 + A_i2 * B2 + A_i3 * B3
#
# Конкретные элементы:
# C_11 = a11 * b1 + a12 * b2 + a13 * b3
# C_21 = a21 * b1 + a22 * b2 + a23 * b3
# C_31 = a31 * b1 + a32 * b2 + a33 * b3
#
# Результат:
# C = [ C_11 ]    [ a11 * b1 + a12 * b2 + a13 * b3 ]
#     [ C_21 ]  = [ a21 * b1 + a22 * b2 + a23 * b3 ]
#     [ C_31 ]    [ a31 * b1 + a32 * b2 + a33 * b3 ]             

# Умножение матрицы A на вектор-столбец B
C = np.dot(A, B)

# Вывод результата
print(C)
